package uk.co.indigo.play.rfid.xml;

public class LabelParserException extends Exception {

	public LabelParserException() {
		super();
	}

	public LabelParserException(String arg0) {
		super(arg0);
	}

	public LabelParserException(Throwable arg0) {
		super(arg0);
	}

	public LabelParserException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
