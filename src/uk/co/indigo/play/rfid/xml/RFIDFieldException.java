package uk.co.indigo.play.rfid.xml;

public class RFIDFieldException extends Exception {

	public RFIDFieldException() {
		super();
	}

	public RFIDFieldException(String arg0) {
		super(arg0);
	}

	public RFIDFieldException(Throwable arg0) {
		super(arg0);
	}

	public RFIDFieldException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
