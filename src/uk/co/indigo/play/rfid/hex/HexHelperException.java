package uk.co.indigo.play.rfid.hex;

public class HexHelperException extends Exception {

	public HexHelperException() {
		super();
	}

	public HexHelperException(String arg0) {
		super(arg0);
	}

	public HexHelperException(Throwable arg0) {
		super(arg0);
	}

	public HexHelperException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
