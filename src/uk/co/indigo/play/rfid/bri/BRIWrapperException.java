package uk.co.indigo.play.rfid.bri;

/**
 * @author milbuw
 *
 */
public class BRIWrapperException extends Exception {

    /**
     * 
     */
    public BRIWrapperException() {
        super();
    }

    /**
     * @param arg0
     */
    public BRIWrapperException(String arg0) {
        super(arg0);
    }

    /**
     * @param arg0
     */
    public BRIWrapperException(Throwable arg0) {
        super(arg0);
    }

    /**
     * @param arg0
     * @param arg1
     */
    public BRIWrapperException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

}
