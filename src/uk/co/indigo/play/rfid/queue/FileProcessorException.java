package uk.co.indigo.play.rfid.queue;

public class FileProcessorException extends Exception {

	public FileProcessorException() {
		super();
	}

	public FileProcessorException(String arg0) {
		super(arg0);
	}

	public FileProcessorException(Throwable arg0) {
		super(arg0);
	}

	public FileProcessorException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
